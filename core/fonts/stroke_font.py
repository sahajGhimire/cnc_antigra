"""
Hershey-style stroke font engine for CNC plotting.

Each glyph is stored as a list of polyline strokes (list of (x, y) tuples).
A stroke of None indicates a pen-up move (gap between strokes).
Coordinates are normalised to a unit height of ~21 units (Hershey standard).
"""

import math

# ---------------------------------------------------------------------------
# Hershey Simplex Glyph Data
# Each entry: char -> {"width": int, "strokes": [[(x,y), ...], ...]}
# Strokes are lists of polylines; each polyline is drawn with pen-down.
# ---------------------------------------------------------------------------

_RAW_GLYPHS = {}


def _define(char, width, strokes):
    """Register a glyph."""
    _RAW_GLYPHS[char] = {"width": width, "strokes": strokes}


