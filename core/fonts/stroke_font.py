GLYPH_HEIGHT = 21.0  # normalised height of glyphs


def get_glyph(char):
    """
    Return glyph data for a character.

    Returns:
        dict with "width" (float) and "strokes" (list of polylines)
        or a fallback rectangle for unsupported chars.
    """
    if char in _RAW_GLYPHS:
        return _RAW_GLYPHS[char]
    # Fallback: small rectangle for unknown characters
    w = 10
    return {
        "width": w,
        "strokes": [[(1, 0), (1, 14), (w - 1, 14), (w - 1, 0), (1, 0)]],
    }


def get_text_paths(text, x=0.0, y=0.0, size=10.0, line_spacing=1.5):
    """
    Convert a text string into a list of path segments suitable for CNC.

    Each path segment is a list of (x, y) tuples representing a
    continuous pen-down polyline. Pen lifts occur between segments.

    Args:
        text: String to render (supports newlines).
        x: Starting X position (mm).
        y: Starting Y position (mm).
        size: Font size in mm (height of capital letters).
        line_spacing: Multiplier for vertical line spacing.

    Returns:
        list of polylines, each polyline is [(x, y), (x, y), ...]
    """
    scale = size / GLYPH_HEIGHT
    paths = []
    cursor_x = x
    cursor_y = y
    char_gap = 2.0 * scale  # gap between characters

    lines = text.split('\n')
    for line_idx, line in enumerate(lines):
        cursor_x = x
        for char in line:
            glyph = get_glyph(char)
            glyph_w = glyph["width"] * scale
            for stroke in glyph["strokes"]:
                polyline = []
                for sx, sy in stroke:
                    # Hershey Y is top-down; invert Y only for capital 'A'
                    px = cursor_x + sx * scale
                    if char == 'A':
                        py = cursor_y + (GLYPH_HEIGHT - sy) * scale
                    else:
                        py = cursor_y + sy * scale
                    polyline.append((round(px, 3), round(py, 3)))
                if len(polyline) >= 2:
                    paths.append(polyline)
            cursor_x += glyph_w + char_gap

        cursor_y -= size * line_spacing  # next line moves down

    return paths


def get_available_chars():
    """Return sorted list of all supported characters."""
    return sorted(_RAW_GLYPHS.keys())
